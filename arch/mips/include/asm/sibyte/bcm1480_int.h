/*
 * The पूर्णांकerrupt mapper deals with 128-bit logical रेजिस्टरs that are
 * implemented as pairs of 64-bit रेजिस्टरs, with the "low" 64 bits in
 * a रेजिस्टर that has an address 0x1000 higher(!) than the
 * corresponding "high" रेजिस्टर.
 *
 * For appropriate रेजिस्टरs, bit 0 of the "high" रेजिस्टर is a
 * cascade bit that summarizes (as a bit-OR) the 64 bits of the "low"
 * रेजिस्टर.
 */

/*
 * This entire file uses _BCM1480_ in all the symbols because it is
 * entirely BCM1480 specअगरic.
 */

